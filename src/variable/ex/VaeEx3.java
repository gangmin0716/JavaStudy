package variable.ex;

public class VaeEx3 {
    public static void main(String[] args) {
        // long 타입의 변수를 선언하고, 그 변수를 10000000000(백억)으로 초기화한 후 출력하세요
        long num1 = 10000000000L;
        System.out.println(num1);

        // boolean 타입의 변수를 선언하고, 그 변수를 true 로 초기화한후 출력하세요
        boolean bool = true;
        System.out.println(bool);
    }
}
